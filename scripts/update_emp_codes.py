def main():
    print("🚀 بدء تحديث رموز الموظفين...")
    db = SessionLocal()
    success = 0
    errors = []
    
    for emp_no, code in updates:
        try:
            emp = db.query(Employee).filter(Employee.emp_no == emp_no).first()
            if emp:
                emp.emp_code = code
                success += 1
                print(f"✅ {emp_no}: {emp.full_name[:20]} ← {code}")
            else:
                errors.append(f"⚠️ الموظف {emp_no} غير موجود")
        except Exception as e:
            errors.append(f"❌ {emp_no}: {str(e)}")
    
    db.commit()
    db.close()
    
    print(f"\n🎉 تم تحديث {success} موظف بنجاح!")
    if errors:
        print("\n".join(errors[:5]))

if __name__ == "__main__":
    main()
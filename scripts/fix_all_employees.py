# 3. المركز الرئيسي للقيادات
hq = db.query(EmergencyCenter).filter(EmergencyCenter.is_hq == True).first()
hq_codes = ['0', 'A0', 'B0', 'C0', 'D0', 'XW', 'RR', 'ST', 'TT', 'XX', 'Y', 'YY', 'YYY', 'YYYY', 'Z', 'AZ', 'BZ', 'CZ', 'DZ', 'O12', 'O14', 'O14A']

# 4. تطبيق التصحيح
fixed = 0
for emp in db.query(Employee).all():
    if not emp.emp_no:
        continue
    
    # إذا كان من القيادات
    if emp.emp_code in hq_codes:
        emp.center_id = hq.id
        fixed += 1
        print(f"🏢 {emp.full_name[:20]} → HQ ({emp.emp_code})")
    
    # إذا كان من الموظفين العاديين
    elif emp.emp_no in corrections:
        new_code, center_name = corrections[emp.emp_no]
        emp.emp_code = new_code
        emp.center_id = centers[center_name]
        fixed += 1
        print(f"✅ {emp.full_name[:20]} → {center_name} ({new_code})")

db.commit()
print(f"\n🎉 تم تصحيح {fixed} موظف!")

# 5. إحصائيات نهائية
